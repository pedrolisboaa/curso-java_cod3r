package classe;

public class Data {

	public Integer dia;
	public String mes;
	public Integer ano;
	
	public Data() {
		this.dia = 1;
		this.mes = "janeiro";
		this.ano = 1970;
	}
	
	public Data (Integer dia, String mes, Integer ano) {
		this.dia = dia;
		this.mes = mes;
		this.ano = ano;
	}
	
	public String obterDataFormata() {
		return dia + "/" + mes + "/" + ano;
	}

	@Override
	public String toString() {
		return dia + "/" + mes + "/" + ano;
	}
	
}
